short unsigned int heart[36] = {0xFFFF, 0x2104, 0x2104, 0xFFFF, 0x2104, 0x2104, 0x0882, 0xC905, 0xC905, 0x0882, 0xEE79, 0xC905, 0x0882, 0xC905, 0xC905, 0xC905, 0xC905, 0xC905, 0xFFFF, 0xA125, 0xC905, 0xC905, 0xC905, 0xC905, 0xFFFF, 0xFFFF, 0xC905, 0xC905, 0xC905, 0xFFFF, 0xFFFF, 0xFFFF, 0x1082, 0xC905, 0xFFFF, 0xFFFF};


void plot_image_heart(int x, int y) {
    for (int i = 0; i < 6; i++) {
        for (int j = 0; j < 6; j++) {
            plot_pixel(x + j, y + i, heart[i * 6 + j]);
        }
    }
}

void erase_image_heart(int x, int y) {
    for (int i = 0; i < 6; i++) {
        for (int j = 0; j < 6; j++) {
            plot_pixel(x + j, y + i, 0);
        }
    }
}
